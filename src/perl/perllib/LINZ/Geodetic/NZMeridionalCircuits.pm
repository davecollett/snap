package LINZ::Geodetic::NZMeridionalCircuits;

=head1 LINZ::Geodetic::NZMeridonalCircuits

Determine the meridional circuits that apply at a given latitude and longitude.

=head2 Synopsis

  $circuits=LINZ::Geodetic::NZMeridionalCircuits::Circuits($lon,$lat);
  print join(' ',@$circuits),"\n";

=cut


sub Circuits
{
    my($lon,$lat)=@_;
    my @circuits=();
    my $data0=tell DATA;
    while( my $line=<DATA> )
    {
        my @parts=split(' ',$line);
        my $code=$parts[0];
        next if $code !~ /^\w+TM2000$/ || @parts < 6;
        my $npts=$parts[5];
        # Check bounding box
        if( $lon < $parts[1] || $lon > $parts[2]
            || $lat < $parts[3] || $lat > $parts[4] )
        {
            my $skip=<DATA> while $npts--;
            next;
        }
        $line=<DATA>;
        my($lon0,$lat0)=split(' ',$line);
        my $less0=$lat < $lat0 ? 1 : 0;
        my $inside=0;
        $npts--;
        while( $npts-- )
        {
            my($lon1,$lat1)=split(' ',$line);
            my $less1=$lat < $lat1 ? 1 : 0;
            if( $less0 != $less1 )
            {
                my $lonx=$lon0+($lon1-$lon0)*($lat-$lat0)/($lat1-$lat0);
                $inside=1-$inside if $lonx < $lon;
            }
            $lon0=$lon1;
            $lat0=$lat1;
            $less0=$less1;
        }
        push(@circuits,$code);
    }
    seek DATA,$data0,0;
    return wantarray ? @circuits : \@circuits;
}

1;

